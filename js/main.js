document.addEventListener('DOMContentLoaded', () => {
    console.log("DOM fully loaded and parsed");

    // State
    let currentLang = 'es';

    // DOM Elements
    const menuToggle = document.querySelector('.menu-toggle');
    const themeWrapper = document.getElementById("themeWrapper");
    const navLinks = document.querySelector('.nav-links');
    const langToggle = document.getElementById('lang-toggle');

    // Mobile Menu Toggle
    if (menuToggle && navLinks) {
        menuToggle.addEventListener('click', () => {
            navLinks.classList.toggle('active');
            const icon = menuToggle.querySelector('i');
            if (navLinks.classList.contains('active')) {
                icon.classList.remove('fa-bars');
                icon.classList.add('fa-times');
            } else {
                icon.classList.remove('fa-times');
                icon.classList.add('fa-bars');
            }
        });

        // Close menu when clicking a link
        document.querySelectorAll('.nav-links a').forEach(link => {
            link.addEventListener('click', () => {
                navLinks.classList.remove('active');
                const icon = menuToggle.querySelector('i');
                if (icon) {
                    icon.classList.remove('fa-times');
                    icon.classList.add('fa-bars');
                }
            });
        });
    }

    // 3D Tilt Effect for Cards
    const tiltCards = document.querySelectorAll('.tilt-card');
    tiltCards.forEach(card => {
        card.addEventListener('mousemove', (e) => {
            const rect = card.getBoundingClientRect();
            const x = e.clientX - rect.left;
            const y = e.clientY - rect.top;

            const centerX = rect.width / 2;
            const centerY = rect.height / 2;

            const rotateX = ((y - centerY) / centerY) * -10; // Max 10deg rotation
            const rotateY = ((x - centerX) / centerX) * 10;

            card.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) scale(1.02)`;
        });

        card.addEventListener('mouseleave', () => {
            card.style.transform = 'perspective(1000px) rotateX(0) rotateY(0) scale(1)';
        });
    });

    // Scroll Animations
    const observerOptions = {
        threshold: 0.1,
        rootMargin: "0px 0px -50px 0px"
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
                observer.unobserve(entry.target);
            }
        });
    }, observerOptions);

    document.querySelectorAll('.section, .hero-content').forEach(section => {
        section.classList.add('hidden');
        observer.observe(section);
    });

    // Language Switching
    if (langToggle) {
        langToggle.addEventListener('click', () => {
            console.log("Language toggle clicked");
            currentLang = currentLang === 'es' ? 'en' : 'es';
            updateContent();
            langToggle.textContent = currentLang === 'es' ? 'EN' : 'ES';
        });

        // Initialize button text
        langToggle.textContent = 'EN';
    } else {
        console.error("Language toggle button not found");
    }

    function updateContent() {

        const elements = document.querySelectorAll('[data-i18n]');

        // 1 Fade + Blur OUT
        elements.forEach((el, index) => {
            setTimeout(() => {
                el.classList.add('lang-transition');
            }, index * 30); // micro delay en cascada
        });

        //  Delay principal (0.6s)
        setTimeout(() => {

            elements.forEach((el, index) => {

                const key = el.getAttribute('data-i18n');
                const translation = translations[currentLang][key];
                if (!translation) return;

                // Cambiar texto
                if (translation.includes("<")) {
                    el.innerHTML = translation;
                } else {
                    el.textContent = translation;
                }

                // Guardar texto para glitch
                el.setAttribute("data-text", el.textContent);

                // Aplicar glitch
                el.classList.add("lang-glitch");

                // Micro-delay cascada entrada
                setTimeout(() => {
                    el.classList.remove('lang-transition');
                    el.classList.add('lang-show');
                }, index * 40);

                // Quitar glitch
                setTimeout(() => {
                    el.classList.remove("lang-glitch");
                }, 400);

            });

            document.documentElement.lang = currentLang;

        }, 600); // delay principal aumentado
    }

    const demoButtons = document.querySelectorAll(".demo-btn");

    demoButtons.forEach(button => {
        button.addEventListener("click", function () {

            const demoId = this.getAttribute("data-demo");
            const videoId = this.getAttribute("data-video");
            const demoContainer = document.getElementById(demoId);

            if (!demoContainer) return;

            // Si está abierto → cerrar y destruir iframe
            if (demoContainer.classList.contains("active")) {

                demoContainer.classList.remove("active");
                demoContainer.innerHTML = ""; //  destruye iframe

                this.textContent = translations[currentLang].btn_view_demo;

            } else {

                // Crear iframe dinámicamente
                const iframe = document.createElement("iframe");
                iframe.src = `https://www.youtube.com/embed/${videoId}?autoplay=1`;
                iframe.title = "ITVision Demo";
                iframe.frameBorder = "0";
                iframe.allow =
                    "accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture";
                iframe.allowFullscreen = true;

                demoContainer.appendChild(iframe);
                demoContainer.classList.add("active");

                this.textContent = translations[currentLang].btn_hide_demo;

                // Scroll suave
                setTimeout(() => {
                    demoContainer.scrollIntoView({
                        behavior: "smooth",
                        block: "center"
                    });
                }, 200);
            }
        });
    });

    const toggle = document.getElementById("themeToggle");
    const body = document.body;
    const modeText = document.getElementById("modeText");

    //  POR DEFECTO: DARK MODE ACTIVADO
    let savedTheme = localStorage.getItem("theme");

    if (!savedTheme || savedTheme === "dark") {
        body.classList.add("dark-mode");
        body.classList.remove("light-mode");   // 
        toggle.checked = true;
        modeText.textContent = "ON";
        modeText.classList.add("mode-on");
    } else {
        body.classList.remove("dark-mode");
        body.classList.add("light-mode");      // 
        toggle.checked = false;
        modeText.textContent = "OFF";
        modeText.classList.add("mode-off");
    }

    // Evento toggle
    toggle.addEventListener("change", () => {

        // Activar animación 
        document.body.classList.add("theme-fade");

        setTimeout(() => {
            document.body.classList.remove("theme-fade");
        }, 500);

        if (toggle.checked) {
            body.classList.add("dark-mode");
            body.classList.remove("light-mode");
            localStorage.setItem("theme", "dark");
            modeText.textContent = "ON";
            modeText.classList.remove("mode-off");
            modeText.classList.add("mode-on");
        } else {
            body.classList.remove("dark-mode");
            body.classList.add("light-mode");
            localStorage.setItem("theme", "light");
            modeText.textContent = "OFF";
            modeText.classList.remove("mode-on");
            modeText.classList.add("mode-off");
        }
    });

    const skillSection = document.querySelector("#skills");
    const bars = document.querySelectorAll(".level-fill");

    const skillObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {

                bars.forEach((bar, index) => {

                    const level = parseInt(bar.getAttribute("data-level"));

                    if (level <= 40) {
                        bar.classList.add("beginner");
                    } else if (level <= 75) {
                        bar.classList.add("intermediate");
                    } else {
                        bar.classList.add("advanced");
                    }

                    setTimeout(() => {
                        bar.style.width = level + "%";
                    }, index * 150);
                });

                skillObserver.unobserve(skillSection);
            }
        });
    }, {
        threshold: 0.1,
        rootMargin: "0px 0px -50px 0px"
    });


    // AQUÍ VA EL FALLBACK PARA MÓVIL
    if (window.innerWidth < 900) {
        bars.forEach((bar, index) => {

            const level = parseInt(bar.getAttribute("data-level"));

            if (level <= 40) {
                bar.classList.add("beginner");
            } else if (level <= 75) {
                bar.classList.add("intermediate");
            } else {
                bar.classList.add("advanced");
            }

            setTimeout(() => {
                bar.style.width = level + "%";
            }, index * 150);
        });
    } else {
        // Solo usar observer en pantallas grandes
        if (skillSection) {
            skillObserver.observe(skillSection);
        }
    }



    const nav = document.querySelector("nav");

    function handleResponsiveTogglePlacement() {
        if (window.innerWidth <= 1000) {
            if (!navLinks.contains(themeWrapper)) {
                navLinks.appendChild(themeWrapper);
            }
        } else {
            if (!nav.contains(themeWrapper)) {
                nav.insertBefore(themeWrapper, document.querySelector(".menu-toggle"));
            }
        }
    }

    // Ejecutar al cargar
    handleResponsiveTogglePlacement();

    // Ejecutar al redimensionar
    window.addEventListener("resize", handleResponsiveTogglePlacement);

});

